# Bitwise Operator & | ^

print('Bitwise AND operator')
print(0 & 0)
print(0 & 1)
print(1 & 0)
print(1 & 1)

print('Bitwise OR operator')
print(0 | 0)
print(0 | 1)
print(1 | 0)
print(1 | 1)

print('Bitwise ExOR operator')
print(0 ^ 0)
print(0 ^ 1)
print(1 ^ 0)
print(1 ^ 1)

print('<< Binary Left Shift')
a = 240
print(a << 2)

print('<< Binary Right Shift')
a = 24
print(a << 2)

print('~ Binary Ones Complement')
a = 60
print(~a)